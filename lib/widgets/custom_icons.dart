class IconPainter extends CustomPainter {
  final String pathData;
  final Color color;

  IconPainter(this.pathData, this.color);

  @override
  void paint(Canvas canvas, Size size) {
    final paint = Paint()
      ..color = color
      ..style = PaintingStyle.fill;

    final path = parseSvgPath(pathData);
    
    final bounds = path.getBounds();
    final scaleX = size.width / bounds.width;
    final scaleY = size.height / bounds.height;
    final scale = scaleX < scaleY ? scaleX : scaleY;
    
    final matrix = Matrix4.identity()
      ..translate(
        (size.width - bounds.width * scale) / 2 - bounds.left * scale,
        (size.height - bounds.height * scale) / 2 - bounds.top * scale,
      )
      ..scale(scale);

    canvas.drawPath(path.transform(matrix.storage), paint);
  }

  Path parseSvgPath(String pathData) {
    final path = Path();
    final commands = pathData.split(RegExp(r'(?=[MmLlHhVvCcSsQqTtAaZz])'));
    
    double currentX = 0;
    double currentY = 0;
    
    for (var command in commands) {
      if (command.isEmpty) continue;
      
      final type = command[0];
      final coords = command.substring(1)
          .trim()
          .split(RegExp(r'[\s,]+'))
          .where((s) => s.isNotEmpty)
          .map((s) => double.tryParse(s) ?? 0)
          .toList();
      
      switch (type) {
        case 'M':
          if (coords.length >= 2) {
            currentX = coords[0];
            currentY = coords[1];
            path.moveTo(currentX, currentY);
          }
          break;
        case 'm':
          if (coords.length >= 2) {
            currentX += coords[0];
            currentY += coords[1];
            path.moveTo(currentX, currentY);
          }
          break;
        case 'L':
          if (coords.length >= 2) {
            currentX = coords[0];
            currentY = coords[1];
            path.lineTo(currentX, currentY);
          }
          break;
        case 'l':
          if (coords.length >= 2) {
            currentX += coords[0];
            currentY += coords[1];
            path.lineTo(currentX, currentY);
          }
          break;
        case 'H':
          if (coords.isNotEmpty) {
            currentX = coords[0];
            path.lineTo(currentX, currentY);
          }
          break;
        case 'h':
          if (coords.isNotEmpty) {
            currentX += coords[0];
            path.lineTo(currentX, currentY);
          }
          break;
        case 'V':
          if (coords.isNotEmpty) {
            currentY = coords[0];
            path.lineTo(currentX, currentY);
          }
          break;
        case 'v':
          if (coords.isNotEmpty) {
            currentY += coords[0];
            path.lineTo(currentX, currentY);
          }
          break;
        case 'C':
          if (coords.length >= 6) {
            path.cubicTo(coords[0], coords[1], coords[2], coords[3], coords[4], coords[5]);
            currentX = coords[4];
            currentY = coords[5];
          }
          break;
        case 'c':
          if (coords.length >= 6) {
            path.cubicTo(
              currentX + coords[0],
              currentY + coords[1],
              currentX + coords[2],
              currentY + coords[3],
              currentX + coords[4],
              currentY + coords[5],
            );
            currentX += coords[4];
            currentY += coords[5];
          }
          break;
        case 'S':
          if (coords.length >= 4) {
            path.quadraticBezierTo(coords[0], coords[1], coords[2], coords[3]);
            currentX = coords[2];
            currentY = coords[3];
          }
          break;
        case 's':
          if (coords.length >= 4) {
            path.quadraticBezierTo(
              currentX + coords[0],
              currentY + coords[1],
              currentX + coords[2],
              currentY + coords[3],
            );
            currentX += coords[2];
            currentY += coords[3];
          }
          break;
        case 'Z':
        case 'z':
          path.close();
          break;
      }
    }
    
    return path;
  }

  @override
  bool shouldRepaint(IconPainter oldDelegate) =>
      pathData != oldDelegate.pathData || color != oldDelegate.color;
}